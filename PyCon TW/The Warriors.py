class Warrior:
    def __init__(self):
        self.health = 50
        self.attack = 5

    @property
    def is_alive(self):
        return self.health > 0


class Knight(Warrior):
    def __init__(self):
        self.health = 50
        self.attack = 7


def fight(w1, w2):
    while w1.is_alive and w2.is_alive:
        w2.health -= w1.attack
        if w2.is_alive:
            w1.health -= w2.attack
    return w1.is_alive


