def checkio(data):
    m = ["", "M", "MM", "MMM"]
    c = ["", "C", "CC", "CCC", "CD", "D",
         "DC", "DCC", "DCCC", "CM"]
    x = ["", "X", "XX", "XXX", "XL", "L",
         "LX", "LXX", "LXXX", "XC"]
    i = ["", "I", "II", "III", "IV", "V",
         "VI", "VII", "VIII", "IX"]

    ths = m[data // 1000]
    hs = c[(data % 1000) // 100]
    ts = x[(data % 100) // 10]
    os = i[data % 10]

    return ths + hs + ts + os


